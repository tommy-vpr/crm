"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { signOut, useSession } from "next-auth/react";
import { cn } from "@/lib/utils";
import { Avatar } from "@/components/ui/index";
import { useUIStore } from "@/stores/ui";
import { NotificationBell } from "@/components/layout/notification-bell";

const navItems = [
  { href: "/dashboard", label: "Dashboard", icon: DashboardIcon },
  { href: "/deals", label: "Deals", icon: DealIcon },
  { href: "/contacts", label: "Contacts", icon: ContactIcon },
  { href: "/companies", label: "Companies", icon: CompanyIcon },
  { href: "/pipeline", label: "Pipeline", icon: PipelineIcon },
  { href: "/tasks", label: "Tasks", icon: TaskIcon },
  { href: "/analytics", label: "Analytics", icon: AnalyticsIcon },
  { href: "/settings", label: "Settings", icon: SettingsIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();
  const { sidebarOpen, setCommandPaletteOpen } = useUIStore();

  if (!sidebarOpen) return null;

  return (
    <aside className="flex h-screen w-60 flex-col border-r bg-white">
      {/* Logo */}
      <div className="flex h-14 items-center border-b px-4">
        <h1 className="text-lg font-bold text-slate-900">Cultivated</h1>
        <span className="ml-1.5 text-xs font-medium text-primary">CRM</span>
      </div>

      {/* Search trigger */}
      <div className="px-2 pt-3 pb-1">
        <button
          onClick={() => setCommandPaletteOpen(true)}
          className="flex w-full items-center gap-2 rounded-md border bg-slate-50 px-3 py-2 text-sm text-slate-400 transition hover:bg-slate-100"
        >
          <svg className="h-3.5 w-3.5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z" />
          </svg>
          Search...
          <kbd className="ml-auto rounded border bg-white px-1 text-[10px] font-medium">⌘K</kbd>
        </button>
      </div>

      {/* Nav */}
      <nav className="flex-1 space-y-0.5 px-2 py-3">
        {navItems.map(({ href, label, icon: Icon }) => {
          const isActive = pathname.startsWith(href);
          return (
            <Link
              key={href}
              href={href}
              className={cn(
                "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
                isActive
                  ? "bg-primary/10 text-primary"
                  : "text-slate-600 hover:bg-slate-100 hover:text-slate-900"
              )}
            >
              <Icon className="h-4 w-4" />
              {label}
            </Link>
          );
        })}
      </nav>

      {/* User */}
      {session?.user && (
        <div className="border-t p-3">
          <div className="flex items-center gap-3">
            <Avatar name={session.user.name || "User"} src={session.user.image} size="sm" />
            <div className="min-w-0 flex-1">
              <p className="truncate text-sm font-medium text-slate-900">
                {session.user.name}
              </p>
              <p className="truncate text-xs text-slate-500">{session.user.email}</p>
            </div>
            <NotificationBell />
          </div>
          <button
            onClick={() => signOut({ callbackUrl: "/login" })}
            className="mt-2 w-full rounded-md px-3 py-1.5 text-left text-xs font-medium text-slate-500 transition hover:bg-slate-100 hover:text-slate-700"
          >
            Sign out
          </button>
        </div>
      )}
    </aside>
  );
}

